// Some code from http://mobiforge.com/developing/story/consuming-json-services-android-apps

package gov.ca.cpuc.calspeed.android;

import java.io.BufferedReader;
import java.io.InputStream;
import java.io.InputStreamReader;

import org.apache.http.HttpEntity;
import org.apache.http.HttpResponse;
import org.apache.http.StatusLine;
import org.apache.http.client.HttpClient;
import org.apache.http.client.methods.HttpGet;
import org.apache.http.impl.client.DefaultHttpClient;

import android.os.AsyncTask;
import android.util.Log;

public class ReverseGeo extends AsyncTask<String, Void, String> {

	public String readJSONFeed(String URL) {
		
		StringBuilder stringBuilder = new StringBuilder();
		HttpClient httpClient = new DefaultHttpClient();
		HttpGet httpGet = new HttpGet(URL);
		try {
			HttpResponse response = httpClient.execute(httpGet);
			StatusLine statusLine = response.getStatusLine();
			int statusCode = statusLine.getStatusCode();
			
			if (statusCode == 200 || statusCode == 0) {
				HttpEntity entity = response.getEntity();
				InputStream inputStream = entity.getContent();
				BufferedReader reader = new BufferedReader(
						new InputStreamReader(inputStream));
				String line;
				while ((line = reader.readLine()) != null) {
					stringBuilder.append(line);
				}
				inputStream.close();
			} else {
				Log.d("Viewer", "Failed to download file");
			}
		} catch (Exception e) {
			Log.d("Viewer", e.getLocalizedMessage());
		}
				
		return stringBuilder.toString();
	}

	@Override
	protected String doInBackground(String... urls) {
		return readJSONFeed(urls[0]);
	}
	
}
